class Solution:
    def depthSum(self, nestedList: List[NestedInteger]) -> int:
        
        """
        recursive function having params input and current depth 
        1. if integer, compute value and add to answer
        if nestedInteger, call recursive function while increasing depth by 1

        """
        ans = [0]
        self.helper(nestedList, ans, 1)

        return ans[0]

    def helper(self, input_int, ans, depth):


        for num in input_int:
            if num.isInteger():
                ans[0] += num.getInteger()*depth
            
            else:
                self.helper(num.getList(), ans, depth + 1)




